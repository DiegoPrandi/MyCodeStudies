# Uma certa importância será dividida entre três ganhadores de
# um concurso. Sendo que da quantia total:
# • O primeiro ganhador recebera 46%;
# • O segundo recebera 32%;
# • O terceiro recebera o restante;
# Elabore um programa que dado o valor do concurso em reais ele,
# calcule e imprima a quantia ganha por cada um dos ganhadores

valor_do_concurso = 1500.0

ganhador1 = 0.46
ganhador2 = 0.32
ganhador3 = 0.22

print(f"Primeiro ganhador = {valor_do_concurso * ganhador1}")
print(f"Segundo ganhador  = {valor_do_concurso * ganhador2}")
print(f"Terceiro ganhador = {valor_do_concurso * ganhador3}")
print(f"Premio total      = {valor_do_concurso}")